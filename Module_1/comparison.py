# >,<,>=,<=,==,!=

x = 5
y = 10

# Equal to
print(x == y)  # False

# Not equal to
print(x != y)  # True

# Greater than
print(x > y)   # False

# Less than
print(x < y)   # True

# Greater than or equal to
print(x >= y)  # False

# Less than or equal to
print(x <= y)  # True


# POWER
power = 2**3
print(power)